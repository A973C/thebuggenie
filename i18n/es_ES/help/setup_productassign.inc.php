<b>Asignación de usuarios a ediciones y componentes</b><br>
En la página resumen de ediciones y componentes, puede buscar desarrolladores/usuarios. Introduzca los detalles a buscar, luego haga clic en el nombre de usuario cuando 
aparezca. Esto abre un menú desplegable, donde puede seleccionar a donde asignar el usuario. <br>
Cuando un problema es publicado en un projecto donde el usuario está asignado, éste será notificado. Lo mismo ocurre cuando un problema es actualizado. Los usuarios en esta lista aparecerán también en la lista de "desarrolladores del projecto"
en la página de resumen del projecto.
