<div style="padding: 2px;"><b>¿Qué debe contener el resumen del problema?</b><br>
El resumen de debe ser una síntesis de su problema en frases cortas o en pocas palabras. Los buenos resúmenes hacen m&acute;s f&acute;cil la identificación del problema o cuestión que 
 está informando y ayuda a los desarrolladores a concentrarse en la solución del problema en lugar de tener que dilusidar lo que quiere decir.<br>
<br>
<b>¿Tiene algunos ejemplos de <u>buenos</u> resúmenes de problemas?</b>
<ul>
	<li>El programa X se cuelga cuando intentaba abrir un archivo PDF</li>
	<li>Un mensaje de error aparece cuando presiono el botón "Imprimir"</li>
	<li>Error de ortografía en el título de la página Y</li>
</ul>
<br>
<b>¿Tiene algunos ejemplos de <u>malos</u> resúmenes de problemas?</b>
<ul>
	<li>El programa X se traba</li>
	<li>El mensaje de error aparece todo el tiempo!!!!!!!!!!!!!</li>
	<li>Este programa está defectuoso</li>
</ul>
