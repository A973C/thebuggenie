<b>Visión general del projecto</b><br>
Para entender mejor como está hecho un projecto, comencemos con un ejemplo, el cual llamaremos <i>"Mi Sistema Operativo"</i>. Buenísimo, un sistema opeartivo.<br>
<i>"Mi Sistema Operativo"</i> viene en varias ediciones diferentes: <i>"Ultimate edition"</i>, <i>"Home edition"</i> y así sucesivamente. Tomemos esto para el ejemplo.<br>
<br>
<i>"Mi Sistema Operativo"</i> está hecho con varios componentes, tales como el  <i>"Kernel"</i>, <i>"Drivers"</i>, <i>"Aplicaciones"</i> y quizás algunos más. Además, la <i>"Ultimate Edition"</i> también tiene un componente de <i>"Aplicaciones Extra"</i>, las cuales no están disponibles en la <i>"Home Edition"</i>. Esto es mejor para los usuarios de Home. Seguro. De todas maneras, esto es lo que se decidió.<br>
<br>
Todavía con nosotros? <b>Excelente!</b> Sigamos.<br>
<br>
<b>Compilaciones</b><br>
A medida que desarrolle <i>"Mi Sistema Operativo"</i>, necesitará liberar algunas versiones aquí y allá, solo para comprobar que todo funciona. Quizás tenga usuarios impacientes, deseosos de comprobar el progreso realizado desde ayer. Estas "versiones" son llamadas "compilaciones".<br>
<br>
Una compilación tiene un número de versión, que nos dice en que parte del ciclo esa compilación fue liberada.<br>
<b>¿Qué se necesita para un projecto?</b><br>
Como se puede ver abajo, BUGS 2 soporta todas estas partes. Esto es lo necesario para informar un problema:
<ul>
	<li>Uno o más projectos</li>
	<li>Los projectos deben tener al menos una edición</li>
	<li>La edición debe tener al menos un componente</li>
	<li>La edición debe tener al menos una liberación (compilación)</li>
</ul>
