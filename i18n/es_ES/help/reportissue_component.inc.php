<div style="padding: 2px;"><b>¿Qué es un "componente"?</b><br>
Una pieza de software está generalmente dividida en varias "subpartes", llamadas "componentes". Al dividir el software en estos componentes, un desarrollador
o un equipo de desarrolladores puede enfocarse en un área del software, mientras otros desarrolladores se focalizan en otras.<br>
<br>
Si selecciona el componente correcto, las posibilidades de que el desarrollador correcto sea notificado inmediatamente son mayores, lo que incrementa las probabilidades de que
su problema sea revisado.<br> 
<br>
<b>¿Cómo sé que componente elegir?</b><br>
Los componentes por lo general tienen nombres autodescriptivos, como "menú", "impresión", etc - esto varía dependiendo del tipo de software que esté utilizando
y podría ser cualquier cosa. Busque un componente que se aproxime al área que Ud. está informando. <i>Si no puede encontrar el componente correcto</i>, 
por lo general existe el componente "general" o "desconocido", elija uno.<br>
