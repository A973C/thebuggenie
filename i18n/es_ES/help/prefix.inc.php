Si se especifica un prefijo, cada problema informado para un projecto tendrá el prefijo delante del número de problema.<br>
<br>
Se puede dejar vacío, pero esto no es conveniente, se debe mantener abreviado.<br>
<br>
<b>Por ejemplo</b><br>
Supongamos que tiene 3 projectos, <i>Projecto A</i>, <i>Projecto B</i> y <i>Projecto C</i>. Usted entonces quiere un prefijo para sus projectos:<br>
<ul>
<li>Projecto A tiene el prefijo <b>PA</b></li>
<li>Projecto B tiene el prefijo <b>PB</b></li>
<li>Projecto C tiene el prefijo <b>PC</b></li>
</ul>
Cuando el primer problema sea informado en el <i>Projecto A</i>, el número de problema que tendrá será <b>PA-1</b>. El próximo problema será llamado <b>PA-2</b>. El primer problema informado en el <i>Projecto B</i> se llamará por supuesto <b>PB-1</b>, y el próximo será numerado correctamente - <b>PB-2</b>.<br>
<br>
Al usar un prefijo en sus projectos, puede fácilmente ver - sólamente al mirar el número - que projecto es el afectado.<br>
<br>
Si no quiere utilizar un prefijo, cada problema será numerado secuencialmente. El primer problema informado será el  <b>#1</b>, el próximo <b>#2</b> - sin identificar el projecto afectado. Por supuesto que se puede tener un projecto con prefijo, y otro sin, combínelos si prefiere.
