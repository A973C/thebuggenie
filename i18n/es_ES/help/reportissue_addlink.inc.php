<div style="padding: 2px;"><b>¿Cómo agrego un enlace a mi informe?</b><br>
Si quiere incluir un enlace a una página web u otro enlace a su informe de problema, pegue la URL (cópiela desde la barra de direcciones de 
su navegador, por ejemplo) en el campo <b>URL</b>, y luego escriba una breve descripción acerca de lo que la URL contiene. Si no lo hace 
, el contenido de la URL será utilizado.<br>
