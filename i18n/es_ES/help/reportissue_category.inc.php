<div style="padding: 2px;"><b>¿Qué es una "categoría"?</b><br>
Por lo general los desarrolladores dividen los problemas en categorías, por razones administrativas y estadísticas. 
Al seleccionar la categoría correcta, puede ayudar a los desarrolladores a encontrar su problema más rápido.<br>
<br>
<b>¿Cómo sé que categoría elegir?</b><br>
Las categorías por lo general tienen nombres autodescriptivos. Busque la categoría que más se ajuste a su problema. 
<i>Si no puede encontrar la categoría correcta</i>, existen las categorías "general" o "desconocido"  - elija cualquiera.<br>
