<div style="padding: 2px;"><b>¿Qué es una "compilació"?</b><br>
Al evolucionar el projecto, las más nuevas  "compilaciones" pueden ser liberadas. Una "compilación" es una liberación para un momento dado, y cada  "compilación" se  
identifica por su correspondiente  "número de versión".<br>
<br>
Puede que esto lo haya visto en el software que este usando, números de versi&ocute;n como  "1.1", "2.1.3", etc. Esta información
está generalmente disponible en la opción del menú "Acerca de...".<br>
<br>
<b>Ej.:</b> Si hace clic en el enlace "Acerca de..." en esta página, verá la "compilación" que tiene este programa. (Si lo hace, 
puede hacer clic en el enlace "Continuar informando" para volver a la página del informe)<br>  
<br>
<b>¿Qué compilación selecciono?</b><br>
Intente seleccionar el mismo número de compilación que la aplicación que está utilizando. Utilice la información de esta página para encontrar 
el número de versión. Si no está seguro, pero sabe que tiene la última vrsión, utilice "Por defecto".<br>
<br>
<b>La compilación que quiero informar no está en la lista</b><br>
En este caso, tal vez no sea posible informar el problema para esa compilación. Si esto sucede, comuníquese con los desarrolladores, 
registrándose en la página; y enviando un correo electrónico, o buscando el enlace "contáctenos".<br>
