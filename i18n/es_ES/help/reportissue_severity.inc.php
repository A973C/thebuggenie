<div style="padding: 2px;"><b>¿Qué significa "gravedad"?</b><br>
Generalmente hay diferentes "niveles de gravedad" los cuales le indican al desarrollador que tan serio/crítico es el problema. Debe ser conservador cuando seleccione
un nivel de gravedad - lo que parece grave podría no ser tan grave después de todo, y puede incluso tener una fácil y bastante obvia solución.<br>
<br>
<b>¿Cómo puedo saber que "nivel de gravedad" seleccionar?</b><br>
Normalmente tienen nombres que los autodefinen, como "Crítico", "Mayor", "Menor", etc. busque un nombre que se ajuste al problema<br>
<br>
<i>Debe tener en cuenta que "Crítico" está generalmente reservado para el problema que altera el funcionamiento correcto o detiene totalmente el software, y "Mayor"
está generalmente reservado para las cuestiones que tienen un gran impacto en la forma que funciona el programa</i><br>
<br> 
<u>Si no encuentra el nivel correcto de gravedad</u>, hay un nivel "desconocido" o "menor" - seleccione uno.<br>
