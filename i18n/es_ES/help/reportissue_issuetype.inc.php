<div style="padding: 2px;"><b>¿Qué es un tipo de problema?</b><br>
Un "tipo de problema" es lo que describe la naturaleza del problema que est&acute;informando. Al seleccionar un tipo de problema que se ajuste a lo que está informando, 
 Ud. se asegura que los desarrolladores serán notificados correctamente.<br>
<br>
Hay varios tipos de problemas que se pueden seleccionar, pero generalmente hay algunos similares a "Informe de error", "Nueva funcionalidad", etc. 
Elija la que mejor se ajuste al problema que está informando.<br>
<br>
<br>
<b>Ej: </b>Ud. encontró un error en el software que está utilizando,<br>y quiere informarlo a los desarrolladores para que lo solucionen.<br>
<i><u>Elija "Informe de error"</u></i><br>
<br>
<b>Ej 2:</b>Le falta un elemento importante en el software.<br>Quiere informar esto a los desarrolladores así estos lo agregan en la próxima versión.<br>
<i><u>Elija "Nueva funcionalidad"</u></i><br>
