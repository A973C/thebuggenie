<b>Creación de una edición</b><br>
Antes de informar un problema, necesita tener al menos una edición en su projecto. Si Ud. posee varias ediciones de su projecto, acá es donde 
se configura. Si Ud. solo tiene uno, no se preocupe - lo llamaremos de forma genérica.<br>
<br>
Si Ud. no está allí todavía, haga clic en el enlace <span style="display: float;"><img src="themes/oxygen/cfg_icon_projects.png">&nbsp;<b>Gestión de Projectos</b></span> ,
y luego en <span style="display: float;"><img src="themes/oxygen/cfg_icon_projecteditionsbuilds.png">&nbsp;<b>Ediciones, compilaciones y componentes</b></span>.<br>
<br>
<b>Ediciones, compilaciones y componentes</b><br>
Una lista de todas sus ediciones será mostrada arriba a la izquierda, y una lista de todos sus componentes será mostrada arriba a la derecha.<br>
<br>
<b>Creación de una nueva edición</b><br>
Para crear una nueva edición, introduzca un nombre en el campo "Agregar una edición", y presione el botón más a la derecha del campo para agregarlo.
La edición aparecerá en la lista - haga clic en el nombre. En la nueva página, puede editar la información de la edición, la cual aparecerá en el mismo
lugar como la información de los projectos. La edición también tiene varias opciones, las cuales hacen lo mismo que la configuración de los projectos. (lea  
más acerca de estas opciones en la sección "creación de un projecto").<br>
<br>
<b>IMPORTANTE: Recuerde otorgar permisos a otros usuarios para el projecto.</b><br>El acceso será otorgado al grupo que Ud. pertenece, pero recuerde dar acceso
a otros grupos o usuarios si lo desea.<br>
<br>
<b>Creación de un nuevo componente y asignación a una edición</b><br>
Existen dos maneras de crear componentes. Puede crearlos directamente desde la página de resumen de edición y componentes como ya vió al principio de 
esta sección, o en la página de opciones de edición. Al agregar un componente en la p&aacute:gina de opciones de edición automáticamente se asigna a la edición. Si lo agrega
en la página de resumen de edición y componentes, debe también agregarlo a la edición antes para que los usuarios puedan utilizarlo. Puede agregarlo yendo a
 la página opciones de edición (haga clic en el nombre de la edición en la página resumen de edición y componentes) y clic en el enlace "Agregar esto" próximo al nombre del componente.<br>
<br> 
<b>IMPORTANTE: Recuerde otorgar permisos a otros usuarios para el projecto.</b><br>El acceso será otorgado al grupo que Ud. pertenece, pero recuerde dar acceso
a otros grupos o usuarios si lo desea.<br>
