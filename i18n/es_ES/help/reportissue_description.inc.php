<div style="padding: 2px;"><b>¿Qué debe contener una descripción?</b><br>
La descripción debe dar una buena y completa explicación del problema que está informando. Puntos que pueden estar en la descripción:
<ul>
	<li>porque está informando el problema</li>
	<li>qué sucedió - cual es el problema</li>
	<li>cual sería el comportamiento esperado si el problema no existiera</li>
</ul>
Recuerde que por lo general uno no es demasiado detallado - las descripciones más completas y detalladas son mejores.<br>
También recuerde que puede utilizar la barra de herramientas para dar formato al texto y lo ayudará para describir los pasos para reproducir el problema.<br>
