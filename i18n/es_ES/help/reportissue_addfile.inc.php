<div style="padding: 2px;"><b>¿Cómo agrego un archivo a mi informe?</b><br>
Si quiere incluir un archivo a su informe de problema, haga click en el botón "Examinar" que está junto al campo <b>Archivo</b>. Un navegador de archivos aparecerá. Navegue
hasta el archivo que quiera incluir, y selecciónelo haciendo doble clic, o un clic y presionando ENTER. El diálogo desaparecerá, y el
nombre del archivo se mostrará en el campo <b>Archivo</b>.<br>
<br>
Recuerde completar con una descripción corta acerca del archivo que está adjuntando.<br>
<br>
<b>Cosas a tener en cuenta cuando sube un archivo</b><br>
Hay algunas reglas que debe  tener presente en el momento de subir los archivos:
<ul>
	<li>Los archivos pueden necesitar que sean menores a cierto tamaño</li>
	<li>Los archivos pueden necesitar que sean de cierto tipo</li>
</ul>
Mire el texto que dice algo acerca de estas restricciones, debajo del campo <b>Descripción</b>, por encima del botón "Adjuntar".<br> 
