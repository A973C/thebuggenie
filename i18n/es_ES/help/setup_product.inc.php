<b>Creación de un projecto</b><br>
Para poder informar problemas necesita tener al menos un projecto. O proyecto. En BUGS 2, llamamos al nivel superior un <b>projecto</b>. Esto no significa
que Ud. no puede utilizar BUGS 2 para proyectos, u otras cosas - pero es bueno saber de que estamos hablando.<br>
<br>
Entonces, Ud. necesita un projecto. Si no está allí, haga clic en <span style="display: float;"><img src="themes/oxygen/cfg_icon_projects.png">&nbsp;<b>Gestión de projectos</b></span>.
Aquí, tiene una lista de todos los projectos que hay BUGS 2 (que tiene acceso), y puede crear nuevos projectos. También puede definir que projecto estará 
por defecto para los usuarios cuando ellos informen nuevos problemas.<br>
<br>
Para crear un projecto, introduzca el nombre del projecto o proyecto en el campo <b>"AGREGAR UN PROJECTO"</b>, luego haga clic en el botón <b>Agregar</b> con el signo más. Esto es todo lo que necesita para crear un projecto!<br>
<br>
<b>IMPORTANTE: Recuerde otorgar permisos a otros usuarios para acceder al projecto.</b><br>El acceso será dado al grupo al cual Ud. pertenece, pero recuerde asignar accesos 
a otros grupos o usuarios si lo desea.<br>
<br>
Por supuesto, esto no es todo lo que se puede hacer con los projectos en BUGS 2. Veamos las diferentes opciones. Haga clic en 
<span style="display: float;"><img src="themes/oxygen/cfg_icon_projectsettings.png">&nbsp;<b>Información y ajustes</b></span>.<br>
<br>
<b>Información y ajustes</b><br>
Los primeros cuatro campos son para el nombre, descripción, página principal y web de documentación de su projecto:
<ul>
	<li><b>Descripción</b><br>Aparecerá en la página resumen de su projecto, la que estará disponible desde la portada</li>
	<li><b>Página de inicio</b><br>Aparecerá como un enlace en la página resumen (vea <i>descripción</i>) y el bloque del projecto en la portada</li>
	<li><b>Documentación</b><br>Aparecerá como un enlace "Ver documentación" en la página resumen, y en la portada</li>
</ul>
También tiene opciones para "prefijos". Debe utilizar prefijos - hacen la vida más simple. Más información acerca del uso de prefijos de projectos 
aquí: <a href="?topic=prefix&prev_topic=setup_project"><b>¿Qué es un prefijo?</b></a><br>
<br>
También hay otros ajustes para projectos: 
<ul>
	<li><b>Liderado por</b><br>Es quien está a cargo de este projecto. Puede ser un equipo o un usuario. Los usuarios en el equipo serán notificados de los nuevos problemas, y el
	 usuario/equipo aparecerá en la portada en el bloque informativo de este projecto.</li>
	<li><b>QA por</b><br>Lo mismo que lo anterior, sólo que para el QA</li>
</ul>
También puede asignar desarrolladores a diferentes partes de su projecto - más acerca de esto en la próxima sección (vea "creación de ediciones y componentes")<br>
<ul>
	<li><b>Utilizar tareas en informes de problemas</b><br>Si está en "Sí", puede agregar tareas al informe. Las tareas se pueden dividir el problema en partes más pequeñas, y pueden ser
	 seguidas individualmente. Las tareas poseen estados y asignaciones, y también pueden ser promovidas a problemas individuales, si se las convierte</li>
	<li><b>Permitir votar en los problemas</b><br>Permite que la gente vote en los problemas, permitiendo a los desarrolladores saber que problema es prioritario</li>
	<li><b>Medición de tiempo</b><br>Ajustes de tiempo para saber como debe medirse. Los problemas poseen tiempos "estimados" y "transcurridos".</li>
	<li><b>Horas por día</b><br>Utilizado en conjunto con el anterior cuando éste incluye días- cuantas horas deben incluirse  
	en cada dí</li>
	<li><b>Fecha de liberación</b><br>Seleccione si existe una fecha de liberación planificada, y cuando será. Se mostrará en el resumen del projecto</li>
	<li><b>Liberado</b><br>Si el projecto es liberado o no. Muestra lo mismo que arriba</li> 
	<li><b>Permitir que los problemas se informen</b><br>Tal cual lo que es</li>
	<li><b>Utilizar compilaciones</b><br>Si se usan o no compilaciones</li>
	<li><b>Estado por defecto para los nuevos informes de problemas</b><br>Que estado quiere que sea establecido por defecto</li>
</ul>
