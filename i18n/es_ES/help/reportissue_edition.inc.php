<div style="padding: 2px;"><b>¿Qué edición selecciono?</b><br>
Un projecto puede estar dividido en varias "ediciones", versiones del mismo projecto que tienen diferentes características.
Ud. debe elegir la edición que está informando el problema. Si no está seguro, utilice "por defecto".<br>  
<br>
<b>La edición que quiero informar no está en la lista</b><br>
En este caso, no es posible informar el problema para esa edición. Si es así, intente comunicarse con los desarrolladores, ya sea
registrándose en la página web, o buscando el enlace  "contáctenos".<br>
