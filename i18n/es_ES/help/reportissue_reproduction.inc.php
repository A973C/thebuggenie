<div style="padding: 2px;"><b>¿Qué debe contener los "pasos para reproducir"?</b><br>
Este campo por lo general es opcional. Al completarlo ayuda a los desarrolladores a identificar su problema, permitiéndoles reproducirlo. Si pone en detalle
como operó para "disparar" el problema que está informando, los desarrolladores pueden encontrar y identificar el problema má facilmente.<br>
<br>
Recuerde que puede utilizar la barra de herramienta de formato, debajo del campo, le ayuda para describir los pasos.<br>
<br>
<b>¿Tiene algunos ejemplos de <u>buenos</u> "pasos para reproducir"?</b>
<ul>
	<li>Cuando hago clic en el menú <u>A</u>rchivo-&gt;<u>A</u>brir, y doble clic en un PDF en el navegador que aparece, el Program X aborta con un mensaje de error</li>
	<li>Cuando presiono el botón "Imprimir" en la barra de menú (el que tiene el ícono con la impresora), salta un mensaje de error</li>
	<li>Abro el program X, clic en "ayuda", luego busco "dulce". Abro el segundo ítem en la lista ("golosinas"), luego noto un error de ortografí en la cabecera del segundo párrafo</li>
</ul>
Lo mismo va para los pasos para reproducción como para la descripción de problemas - no puede ser nunca <i>demasiado</i> profundo.<br>
<br>
<i>Si no quiere ingresar nada en este campo, solo haga clic en el botón "Establecer" a la derecha del campo.</i><br>
